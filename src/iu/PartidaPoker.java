    private JButton[] crearArrayBotones() {
        JButton[] botones = new JButton[5];
        botones[0] = jCarta1;
        botones[1] = jCarta2;
        botones[2] = jCarta3;
        botones[3] = jCarta4;
        botones[4] = jCarta5;
        return botones;
    }

